def sum_of_numbers(list_numbers):
    """
    Функція для обчислення суми чисел у рядку, розділених комами.
    Якщо рядок містить нечислові символи, повертає повідомлення про помилку.
    """
    try:
        # Розділити рядок за комами
        numbers = list_numbers.split(',')
        # Перетворити елементи на числа та обчислити їх суму
        total = sum(int(num) for num in numbers)
        return total
    except ValueError:
        # Якщо є нечислові символи, зловити виключення та повернути повідомлення про помилку
        return "Не можу це зробити!"


# Масив зі строками
strings = [
    "1,2,3,4",
    "1,2,3,4,50",
    "qwerty1,2,3"
]

# Для кожного елемента масиву вивести суму чисел або повідомлення про помилку
for s in strings:
    result = sum_of_numbers(s)
    print(result)